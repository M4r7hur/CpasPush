#include "ft_list.h"

t_list	*ft_list_find(t_list *begin_list, void *data_ref, int (*cmp)())
{
	t_list	*list_ptr;

	list_ptr = begin_list;
	while ((*cmp)(list_ptr->data, data_ref) != 0)
	{
		list_ptr = list_ptr->next;
	}
	return (list_ptr);
}
