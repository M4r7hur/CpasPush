int		plus(int x, int y)
{
	return (x + y);
}

int		minus(int x, int y)
{
	return (x - y);
}

int		divide(int x, int y)
{
	return (x / y);
}

int		times(int x, int y)
{
	return (x * y);
}

int		modulo(int x, int y)
{
	return (x % y);
}
